
class Switch2{

	public static void main(String args[])
	{
		String day = "Monday";// compiletime
	
		switch(day)
		{
			case "Monday": System.out.println("Working day");break;
				
			case "Tuesday": System.out.println("CDAC Working day");
			
			case "Wednesday":System.out.println("Formal Working day"); 
			
		
			System.out.println("String use in case option.......");break;
			
			
			
			default: System.out.println(" Chutti hai !");
			
		}
		
	}
	
}