class ForLoop2{

	public static void main(String args[])
	{
		for(int i=0;i<=10;i++)//Asending order
		{
		
		System.out.print(i+ " ");
		
		}
	System.out.println("-----------------------------------");
		for(int i=10;i>=0;i--)// Desending order
		{
		
		System.out.print(i+ " ");
		
		
		}
		
		//System.out.println(" Ho gana Run  !");
	}
	
}